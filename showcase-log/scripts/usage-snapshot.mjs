#!/usr/bin/env node
// usage-snapshot.mjs — harvest exact token usage from Claude Code transcripts
// into a permanent, project-local snapshot (session-log/usage/).
//
// WHY: Claude Code records exact usage (input/output/cache tokens + model) on
// every assistant message in ~/.claude/projects/<flattened-path>/*.jsonl — but
// those transcripts are cleaned up after ~30 days (cleanupPeriodDays). This
// script copies the numbers into session-log/usage/usage.jsonl inside the
// project, where they are permanent. Cost analysis and session-log joins read
// the snapshot, never the live transcripts, so once a message is harvested
// its transcript can expire without any loss.
//
// USAGE:
//   node _scripts/usage-snapshot.mjs             # harvest + rewrite summary
//   node _scripts/usage-snapshot.mjs --auto      # debounced: exits fast (<50ms)
//                                                #   unless last run >24h ago.
//                                                #   Wired to SessionStart/Stop
//                                                #   hooks by /showcase-log.
//   node _scripts/usage-snapshot.mjs --report    # print summary, write nothing
//   --root <dir>         project root (default: parent of this script's dir)
//   --transcripts <dir>  transcript dir override (default: derived from root)
//
// OUTPUT (in <root>/session-log/usage/):
//   usage.jsonl    one row per assistant message. Merge-idempotent: rows are
//                  deduped by API message id, so re-runs only append new
//                  messages and never duplicate. Line 1 is a self-describing
//                  `{"_comment": ...}` record (valid JSON, so line-by-line
//                  JSONL readers still work) — skipped when re-loading rows.
//   summary.md     per-day × per-model rollup, regenerated every run.
//   .state.json    per-file mtimes + last-run stamp (incremental + debounce).
//
// COST MODEL: API list prices (as-of date in PRICING_AS_OF below), USD per 1M
// tokens; cache write billed at 1.25× input rate, cache read at 0.1× input
// rate. This is "API-equivalent value" — what the usage would cost at list
// rates — NOT what a subscription user was billed. For a quick answer, run
// cost-report.mjs.
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';
import { resolvePaths } from './lib/paths.mjs';
import { localDay, fmt, money, PRICING_AS_OF } from './lib/usage.mjs';

const DEBOUNCE_MS = 24 * 60 * 60 * 1000; // --auto: do real work at most daily

// USD per 1M tokens: [input, output], matched by substring of the model id.
const PRICES = [
  ['fable', 10.0, 50.0],
  ['opus', 5.0, 25.0],
  ['sonnet', 3.0, 15.0],
  ['haiku', 1.0, 5.0],
];
const DEFAULT_PRICE = [5.0, 25.0]; // unknown models: priced as opus-tier

// --- args ---
const argv = process.argv.slice(2);
const flag = (name) => argv.includes(name);
const opt = (name) => {
  const i = argv.indexOf(name);
  return i >= 0 && argv[i + 1] ? argv[i + 1] : null;
};
const AUTO = flag('--auto');
const REPORT = flag('--report');

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const P = resolvePaths(opt('--root') || path.resolve(SCRIPT_DIR, '..'));
// Claude Code flattens the project path into a folder name by replacing every
// non-alphanumeric character with '-'.
const flattened = P.ROOT.replace(/[^a-zA-Z0-9]/g, '-');
const TRANSCRIPT_DIR = path.resolve(
  opt('--transcripts') || path.join(os.homedir(), '.claude', 'projects', flattened),
);

const COMMENT_LINE = JSON.stringify({
  _comment: 'Auto-generated by _scripts/usage-snapshot.mjs — do not hand-edit. One row per '
    + 'Claude Code assistant message: exact token usage + API-equivalent cost (list prices, '
    + 'not billed spend). See usage/summary.md for a human-readable rollup, or run '
    + '"node _scripts/cost-report.mjs" for a quick answer.',
});

const readJSON = (p, fallback) => {
  try { return JSON.parse(fs.readFileSync(p, 'utf8')); } catch { return fallback; }
};

// --- debounce (must stay cheap: this runs on every Stop hook) ---
const state = readJSON(P.STATE_FILE, { files: {}, lastRun: 0 });
if (AUTO && Date.now() - (state.lastRun || 0) < DEBOUNCE_MS) process.exit(0);

// --- load existing snapshot (dedupe base) ---
const rows = [];
const seen = new Set();
if (fs.existsSync(P.USAGE_FILE)) {
  for (const line of fs.readFileSync(P.USAGE_FILE, 'utf8').split('\n')) {
    if (!line.trim()) continue;
    try {
      const r = JSON.parse(line);
      if (r._comment) continue; // the self-describing header line, not a data row
      rows.push(r);
      seen.add(r.id);
    } catch { /* skip corrupt line */ }
  }
}

// --- harvest changed transcript files ---
const priceFor = (model) => {
  const hit = PRICES.find(([k]) => model.includes(k));
  return hit ? [hit[1], hit[2]] : DEFAULT_PRICE;
};

let scanned = 0;
let added = 0;
const newFileState = { ...state.files };
if (fs.existsSync(TRANSCRIPT_DIR)) {
  for (const name of fs.readdirSync(TRANSCRIPT_DIR)) {
    if (!name.endsWith('.jsonl')) continue;
    const fp = path.join(TRANSCRIPT_DIR, name);
    const mtime = fs.statSync(fp).mtimeMs;
    if (state.files[fp] === mtime) continue; // unchanged since last harvest
    scanned++;
    newFileState[fp] = mtime;
    const sessionId = path.basename(name, '.jsonl');
    for (const line of fs.readFileSync(fp, 'utf8').split('\n')) {
      if (!line.includes('"usage"')) continue;
      let rec;
      try { rec = JSON.parse(line); } catch { continue; }
      const msg = rec.message;
      const u = msg && msg.usage;
      if (!u || typeof u !== 'object') continue;
      const model = msg.model || 'unknown';
      if (model === '<synthetic>') continue;
      // A multi-block assistant response appears as several JSONL lines that
      // share one API message id and carry the same usage object — dedupe by
      // message id (fall back to record uuid) or usage would be overcounted.
      const id = msg.id || `${rec.sessionId || sessionId}:${rec.uuid}`;
      if (seen.has(id)) continue;
      seen.add(id);
      const inp = u.input_tokens || 0;
      const out = u.output_tokens || 0;
      const cw = u.cache_creation_input_tokens || 0;
      const cr = u.cache_read_input_tokens || 0;
      const [pi, po] = priceFor(model);
      const cost = (inp * pi + out * po + cw * pi * 1.25 + cr * pi * 0.1) / 1e6;
      rows.push({
        id,
        ts: rec.timestamp || null,
        session: rec.sessionId || sessionId,
        model,
        input: inp,
        output: out,
        cache_write: cw,
        cache_read: cr,
        cost_usd: Math.round(cost * 1e6) / 1e6,
      });
      added++;
    }
  }
} else if (!REPORT) {
  console.error(`usage-snapshot: no transcript folder at ${TRANSCRIPT_DIR} — nothing to harvest yet.`);
}

rows.sort((a, b) => String(a.ts).localeCompare(String(b.ts)));

// --- summarize ---
function buildSummary() {
  const byDay = new Map(); // day -> model -> agg
  const byModel = new Map();
  let total = { msgs: 0, input: 0, output: 0, cache_write: 0, cache_read: 0, cost: 0 };
  const bump = (agg, r) => {
    agg.msgs++; agg.input += r.input; agg.output += r.output;
    agg.cache_write += r.cache_write; agg.cache_read += r.cache_read; agg.cost += r.cost_usd;
  };
  const blank = () => ({ msgs: 0, input: 0, output: 0, cache_write: 0, cache_read: 0, cost: 0 });
  for (const r of rows) {
    const day = localDay(r.ts);
    if (!byDay.has(day)) byDay.set(day, new Map());
    const dm = byDay.get(day);
    if (!dm.has(r.model)) dm.set(r.model, blank());
    bump(dm.get(r.model), r);
    if (!byModel.has(r.model)) byModel.set(r.model, blank());
    bump(byModel.get(r.model), r);
    bump(total, r);
  }
  const lines = [];
  lines.push('# Usage summary');
  lines.push('');
  lines.push('This file is auto-generated by `_scripts/usage-snapshot.mjs` — a human-readable');
  lines.push('rollup of the raw per-message data in `usage.jsonl`. Regenerated on every harvest;');
  lines.push('do not hand-edit. For a quick answer instead of reading this file, run');
  lines.push('`node _scripts/cost-report.mjs`.');
  lines.push('');
  lines.push(`Generated ${new Date().toISOString()} from ${fmt(rows.length)} assistant messages.`);
  lines.push(`Costs are **API-equivalent value at list prices as of ${PRICING_AS_OF}** (see pricing`);
  lines.push('table in `_scripts/usage-snapshot.mjs`), not billed spend. Cache write billed at 1.25×');
  lines.push('input rate, cache read at 0.1×. Prices drift over time — treat older projects\' totals');
  lines.push('as directionally right, not exact at today\'s rates.');
  lines.push('');
  lines.push('## Totals by model');
  lines.push('');
  lines.push('| model | msgs | input | output | cache write | cache read | cost |');
  lines.push('|---|--:|--:|--:|--:|--:|--:|');
  for (const [m, a] of [...byModel.entries()].sort((x, y) => y[1].cost - x[1].cost)) {
    lines.push(`| ${m} | ${fmt(a.msgs)} | ${fmt(a.input)} | ${fmt(a.output)} | ${fmt(a.cache_write)} | ${fmt(a.cache_read)} | ${money(a.cost)} |`);
  }
  lines.push(`| **total** | **${fmt(total.msgs)}** | **${fmt(total.input)}** | **${fmt(total.output)}** | **${fmt(total.cache_write)}** | **${fmt(total.cache_read)}** | **${money(total.cost)}** |`);
  lines.push('');
  lines.push('## By day');
  lines.push('');
  lines.push('| date | model | msgs | output | cost |');
  lines.push('|---|---|--:|--:|--:|');
  for (const [day, dm] of [...byDay.entries()].sort()) {
    for (const [m, a] of [...dm.entries()].sort((x, y) => y[1].cost - x[1].cost)) {
      lines.push(`| ${day} | ${m} | ${fmt(a.msgs)} | ${fmt(a.output)} | ${money(a.cost)} |`);
    }
  }
  lines.push('');
  return { text: lines.join('\n') + '\n', total };
}

const { text: summary, total } = buildSummary();

if (REPORT) {
  console.log(summary);
  console.log(`(report only — nothing written; ${added} new message(s) found in ${scanned} changed file(s))`);
  process.exit(0);
}

// --- write ---
fs.mkdirSync(P.USAGE_DIR, { recursive: true });
const jsonlBody = [COMMENT_LINE, ...rows.map((r) => JSON.stringify(r))].join('\n') + '\n';
fs.writeFileSync(P.USAGE_FILE, jsonlBody);
fs.writeFileSync(P.SUMMARY_FILE, summary);
fs.writeFileSync(P.STATE_FILE, JSON.stringify({ files: newFileState, lastRun: Date.now() }));
console.log(
  `usage-snapshot: ${added} new message(s) from ${scanned} changed file(s); ` +
  `snapshot now ${fmt(rows.length)} messages, ${money(total.cost)} API-equivalent.`,
);
